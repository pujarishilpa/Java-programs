class Loops {
    public static void main(String args[]) {
        for(int i = 1; i<=5; i++)
            System.out.println(i);
        int i = 6;
        while(i<=10) {
            System.out.println(i);
            i++;
        }
        int a = 11;
        do {
            System.out.println(a);
            a++;
        }while(a<=15);
    }
}